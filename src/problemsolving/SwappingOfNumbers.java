package problemsolving;

public class SwappingOfNumbers {
    public static void main(String[] args) {
        int a =10;
        int b=18;
        int temp;
        System.out.println("BEFORE SWAPPING " + a +" " + b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("AFTER SWAPPING " + a + " " + b);

    }
}
